/************** [FUNCTIONS] **************/
ld dotProduct(pdd v1, pdd v2) {
	ld res = v1.fi * v2.fi + v1.se * v2.se;
	return res;
}

ld len(pdd v) {
	ld res = v.fi * v.fi + v.se * v.se;
	res = sqrt(res); return res;
}

ld findAngle(pdd c, pdd l, pdd r) {
	pdd vec1 = mp(l.fi - c.fi, l.se - c.se);
	pdd vec2 = mp(r.fi - c.fi, r.se - c.se);
	return acos(dotProduct(vec1, vec2) / len(vec1) / len(vec2));
}

void Input() {
	cin >> n >> R >> r;
}

void Solve() {
	if (r > R) {cout << "NO"; return;}
	if (2*r > R) {if (n == 1) cout << "YES"; else cout << "NO"; return;}
	if (2*r == R) {if (n < 3) cout << "YES"; else cout << "NO"; return;}
	pdd center = mp(0.0L, 0.0L);
	ld y = (ld)(R*R - 2*R*r)/(R - r);
	ld x = sqrt((ld)(R - r) * (R - r) - (ld)r * r - y * y);
	pdd Left = mp(-x, y), Right = mp(x, y);
	ld angle = findAngle(center, Left, Right);
	if (2.0L * PI - angle * n >= -EPS) cout << "YES"; else cout << "NO";
}
