void Input() {
	cin >> s >> nb >> ns >> nc;
	cin >> pb >> ps >> pc >> r;
	n = s.size();
}

void Solve() {
	i64 B = 0, S = 0, C = 0;
	for (auto x: s) {B += (x == 'B'); S += (x == 'S'); C += (x == 'C');}
	i64 ans = 0, top = 1, bot = 1e13;
	while (top <= bot) {
		i64 mid = (top + bot) / 2;
		i64 prices = 0;
		prices += max(mid * B - nb, 0LL) * pb;
		prices += max(mid * S - ns, 0LL) * ps;
		prices += max(mid * C - nc, 0LL) * pc;
		if (prices <= r) {ans = mid; top = mid+1;}
		else bot = mid - 1;
	}
	cout << ans;
}
