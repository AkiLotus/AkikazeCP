/************** [GLOBAL VARIABLES] **************/
i64 C, P, X, L; vector<set<i64>> Adj; vi Traders;
/************************************************/

/************** [FUNCTIONS] **************/


void Input() {
	cin >> C >> P >> X >> L; X--; L--;
	Adj.rsz(C); Traders.rsz(C, 0);
	while (P--) {
		i64 u, v; cin >> u >> v; u--; v--;
		Adj[u].insert(v); Adj[v].insert(u);
		Traders[u]++; Traders[v]++;
	}
}

void Solve() {
	queue<i64> Q; vb vis(C, false);
	Q.push(L); vis[L] = true;
	while (!Q.empty()) {
		i64 z = Q.front(); Q.pop();
		for (auto it=Adj[z].begin(); it!=Adj[z].end(); it++) {
			i64 t = *it; Adj[t].erase(z);
			if (vis[t]) continue;
			if (Adj[t].size() * 2 <= Traders[t]) {vis[t] = true; Q.push(t);}
		}
	}
	if (vis[X]) cout << "leave"; else cout << "stay";
}
