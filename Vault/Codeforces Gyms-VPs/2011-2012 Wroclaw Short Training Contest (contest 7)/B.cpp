/** -----EXTENSIVE FUNCTIONS----- **/
string toStr(i64 z) {
	string res = "";
	if (z == 0) return "0";
	while (z > 0) {
		i64 tmp = z % 10; z /= 10;
		res.insert(0, 1, char(48+tmp));
	}
	return res;
}

/** -----COMPULSORY FUNCTIONS----- **/
void VarInput() {
	ios_base::sync_with_stdio(0); cin.tie(NULL);
	cin >> T; N.resize(T);
	for (i64 i=0; i<T; i++) cin >> N[i];
}

void ProSolve() {
	for (i64 i=0; i<T; i++) {
		i64 n = N[i];
		if (n == 0) {cout << "1\n"; continue;}
		if (n == 1) {cout << "A+B\n"; continue;}
		if (Ans[n] != "Meow") {cout << Ans[n]; continue;}
		string ans = "+B^"; ans += toStr(n);
		for (int i=n-1; i>0; i--) {
			string z = "A";
			if (i != n-1) {z += "^"; z += toStr(n-i);}
			if (i == 1) z += "B";
			else {z += "B^"; z += toStr(i);}
			map<i64, i64> Map;
			for (i64 j=i+1; j<=n; j++) {
				i64 tmp = j;
				for (i64 x=2; x<=sqrt(tmp); x++) {
					while (tmp % x == 0) {
						tmp /= x; Map[-x]++;
					}
				}
				if (tmp != 1) Map[-tmp]++;
			}
			for (i64 j=1; j<=n-i; j++) {
				i64 tmp = j;
				for (i64 x=2; x<=sqrt(tmp); x++) {
					while (tmp % x == 0) {
						tmp /= x; Map[-x]--;
					}
				}
				if (tmp != 1) Map[-tmp]--;
			}
			for (auto it=Map.begin(); it!=Map.end(); it++) {
				i64 x = -(it->first), y = it->second;
				if (y == 0) continue;
				z.insert(0, "x");
				if (y == 1) z.insert(0, toStr(x));
				else {
					string qq = toStr(x); qq += "^";
					qq += toStr(y); z.insert(0, qq);
				}
			}
			ans.insert(0, z); ans.insert(0, "+");
		}
		string firstMono = "A^"; firstMono += toStr(n);
		ans.insert(0, firstMono);
		Ans[n] = ans;
		cout << ans << endl;
	}
}

