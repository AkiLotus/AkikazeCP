void VarInput() {
	cin >> N >> M; T.resize(N);
	for (i64 i=0; i<N; i++) cin >> T[i];
}

void ProSolve() {
	i64 l = 0, r = LINF;
	while (l != r) {
		i64 mid = (l + r) / 2;
		i64 curr = 0;
		for (i64 i=0; i<N; i++) curr += mid / T[i];
		if (curr >= M) r = mid;
		else l = mid + 1;
	}
	cout << l;
}

